#include "main.h"

/**
 * add - function that sums two numbers
 * @a: integer
 * @b: integer
 * Return: sum of a and b
 */
int add(a, b)
{
	return (a + b); 
}

/**
 * sub - function that subtract two numbers
 * @a: integer
 * @b: integer
 * Return: difference of a and b
 */
int sub(a, b)
{
	return (a - b);
}

/**
 * mul - function that multiplies two numbers
 * @a: integer
 * @b: integer
 * Return: multplication of a and b
 */
int mul(a, b)
{
	return (a * b);
}

/**
 * div - division of 2 numbers
 * @a: integer
 * @b: integer
 * Return: division of a and b
 */
int div(a, b)
{
	return (a / b);
}

/**
 * mod - modulo operator
 * @a: integer
 * @b: integer
 * Return: modulo of a and b
 */
int mod(a, b)
{
	return (a % b);
}
